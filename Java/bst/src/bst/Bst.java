public class Bst {
	
	private Node root;
	
	private void insertI(Integer key) {
		if (this.root.getKey() == null) { // Empty tree
			this.key = key;
		} else {
			Bst newNode = new Bst();
			newNode.key = key;
			
		}
	}
}
